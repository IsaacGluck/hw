public boolean only14(int[] nums) {
  
int count = 0;
 
  for(int length = 0; length <= nums.length; length ++)
  {
     if(nums[length] == 1 || nums[length] == 4)
        {count ++;}
     else
        {return false;}
  }
     if (count == nums.length)
     {return true;}
     else
     {return false;}
  
  
}